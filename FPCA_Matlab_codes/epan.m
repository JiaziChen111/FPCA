% ------------------------------------------------------------------------------ 
% Description: Epanechnikov kernel function
%
% ------------------------------------------------------------------------------ 
% Usage:       - 
% ------------------------------------------------------------------------------ 
% Inputs Simulation:      
%t      - timepoints
 
% ------------------------------------------------------------------------------ 
% Output:      
%k     - kernel function

% ------------------------------------------------------------------------------ 
% Keywords:    epanechnikov kernel
% ------------------------------------------------------------------------------ 
% See also:    -  
% ------------------------------------------------------------------------------ 
% Author:      Heiko Wagner, 2015/05/28 
% ------------------------------------------------------------------------------ 

function [k]=epan(t);  
k= 3/4*(1-t.^2);
k((abs(t)-1)>0)=0;
end
